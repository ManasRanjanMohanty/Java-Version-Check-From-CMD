
class Pyramid13 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int i,n=5,k,x;
		
		 
		
		for(i=0;i<=5;i++)
		 
		 { 
			 
			for(k=1;k<=n;k++) 
			    {
			  System.out.print(" ");
			    }
			 
				 
		for(x=1;x<=i;x++)
			 { 
			
			
			
			System.out.print(""+i);
			
			System.out.print(" ");
		 } 
		
		System.out.println(" ");
		n=n-1;
	}
}
}

